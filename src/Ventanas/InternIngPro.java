    private void jButtonIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIngresarActionPerformed
            // Lógica de Ingreso de Producción
        
        // 1. Obtener los valores seleccionados
        int cantidad = (Integer) jSpinner1.getValue();
        
        // Verificación doble del tipo ProductoCombinado
        if (detalleSeleccionado == null || detalleSeleccionado.getIdProducto() == 0) {
            JOptionPane.showMessageDialog(null, "Seleccione un Producto y un Relleno válidos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Validar cantidad
        if (cantidad <= 0) {
            JOptionPane.showMessageDialog(null, "La cantidad a producir debe ser mayor a cero.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // 2. Asignar datos al Modelo de Producción
        Produccion produccion = new Produccion();
        produccion.setIdproducto(detalleSeleccionado.getIdProducto());
        produccion.setIddescp(detalleSeleccionado.getIdDescp());
        produccion.setSaborRelleno(detalleSeleccionado.getSaborRelleno().trim());
        produccion.setCantidadProducida(cantidad);
        
        // 3. Llamar al Controlador
        ctrl_produccion controlador = new ctrl_produccion();
        if (controlador.registrarProduccion(produccion)) {
            String nombreCompleto = detalleSeleccionado.getNombreProducto() + " (" + detalleSeleccionado.getSaborRelleno() + ")";
            JOptionPane.showMessageDialog(null, "Producción de " + cantidad + " unidades de " + nombreCompleto.toUpperCase() + " registrada con éxito.\nEl inventario ha sido actualizado.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            
            // Limpiar campos
            jComboTipoPan.setSelectedIndex(0);
            jComboBox1.setSelectedIndex(0);
            jSpinner1.setValue(1);
        } else {
            JOptionPane.showMessageDialog(null, "Error al registrar la producción.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonIngresarActionPerformed


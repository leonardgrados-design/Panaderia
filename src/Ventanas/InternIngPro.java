    // Método que carga solo los nombres de producto en jComboTipoPan
    private void cargarNombresProductos() {
        ctrl_produccion controlador = new ctrl_produccion();
        List<String> nombres = controlador.obtenerNombresProductos();
        
        jComboTipoPan.removeAllItems();
        jComboTipoPan.addItem("Seleccione Producto");
        
        // Rellenar el combo de nombres de pan
        for (String nombre : nombres) {
            jComboTipoPan.addItem(nombre);
        }
    }
    
    // Método que carga los rellenos (jComboBox1) basados en el producto seleccionado (jComboTipoPan)
    private void cargarRellenosPorProducto(String nombreProducto) {
        // Aseguramos que el modelo sea limpio antes de añadir nuevos items
        jComboBox1.removeAllItems();
        // Usamos la clase ProductoCombinado para el item por defecto
        jComboBox1.addItem(new ProductoCombinado("", "Seleccione Relleno", 0, ""));
        detalleSeleccionado = null; // Resetear la selección
        
        if (!nombreProducto.equals("Seleccione Producto")) {
            ctrl_produccion controlador = new ctrl_produccion();
            // Obtenemos los detalles específicos del producto (incluyendo ID y Sabor)
            List<ProductoCombinado> detalles = controlador.obtenerDetallesPorNombre(nombreProducto);
            
            // Rellenar el combo de rellenos con los objetos ProductoCombinado
            for (ProductoCombinado pc : detalles) {
                jComboBox1.addItem(pc); 
            }
        }
    }

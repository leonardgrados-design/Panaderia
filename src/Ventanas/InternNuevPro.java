    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        
        NuevoProducto NuePro = new NuevoProducto();
        // Se instancia el controlador para usar su método.
        ctrl_nuevpro ctrl_nuepro = new ctrl_nuevpro();
        String sabor = "";
        
        // 1. Obtener Sabor y validar nulos
        if (jComboBox1.getSelectedItem() != null) {
            sabor = jComboBox1.getSelectedItem().toString().trim();
        } else {
            // Si no hay ítems en el combo, el sabor será nulo
             JOptionPane.showMessageDialog(null, "No hay sabores disponibles, verifique la base de datos.");
             return;
        }
        
        // 2. Validar campos obligatorios
        if (txtNombre.getText().trim().isEmpty() || txtPrecio.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Complete los campos Nombre y Precio.");
            return;
        }
        
        // 3. Validar Producto Existente
        if(ctrl_nuepro.existeProducto(txtNombre.getText().trim())){
            JOptionPane.showMessageDialog(null, "El producto ya existe en la base de datos");
            return;
        }
        
        // 4. Validar Selección de Sabor
        if (sabor.equalsIgnoreCase("Seleccione Sabor:")) {
            JOptionPane.showMessageDialog(null, "Seleccione un sabor válido.");
            return;
        }
        
        // 5. Procesar Precio
        float precio = 0;
        try {
            String PrecioTXT = txtPrecio.getText().trim();
            boolean aux = false;
            
            // Logica para reemplazar la coma por punto para el parseo de Float.
            for(int i = 0; i < PrecioTXT.length(); i++){
                if(PrecioTXT.charAt(i)==','){
                    PrecioTXT = PrecioTXT.replace(",",".");
                    aux = true;
                    break; 
                }
            }
            
            // Si el precio contiene una coma/punto, se parsea. Si no, se intenta parsear directamente.
            precio = Float.parseFloat(PrecioTXT);
            
            // 6. Asignar valores al Modelo
            NuePro.setNombre(txtNombre.getText().trim());
            NuePro.setPrecio(precio);
            NuePro.setSaborRelleno(sabor); 
            
            // 7. Llamar al método Guardar
            if(ctrl_nuepro.guardar(NuePro)) {
                JOptionPane.showMessageDialog(null, "Producto guardado con éxito.");
                // Limpiar campos después de guardar
                txtNombre.setText("");
                txtPrecio.setText("");
                jComboBox1.setSelectedIndex(0);
            } else {
                JOptionPane.showMessageDialog(null, "Error al guardar el producto. Intente nuevamente.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El precio debe ser un valor numérico válido (ej. 10 o 10.50).", "Advertencia", JOptionPane.WARNING_MESSAGE);
            System.err.println("Error de formato de número en precio: " + e.getMessage());
        } catch (Exception e) {
             JOptionPane.showMessageDialog(null, "Ocurrió un error inesperado al guardar.", "Error", JOptionPane.ERROR_MESSAGE);
             System.err.println("Error al procesar los datos de nuevo producto: " + e.getMessage());
             e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButtonGuardarActionPerformed



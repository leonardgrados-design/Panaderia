    //METODO PARA CARGAR SABORES
    
   private void cargarComboSabores(){
        Connection cn = Conexion.conecta();
        
        // CORRECCIÓN CLAVE: Usamos DISTINCT para obtener solo los sabores únicos.
        String sql = "SELECT DISTINCT sabor_relleno FROM descripcion_producto ORDER BY sabor_relleno ASC";
        Statement st;
        
        try {
            
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            jComboBox1.removeAllItems();
            jComboBox1.addItem("Seleccione Sabor:" ); // Se agrega el item por defecto
            
            while(rs.next()){
                String sabor = rs.getString("sabor_relleno");
                // Aseguramos que los sabores se guarden con la primera letra en mayúscula por consistencia.
                if (sabor != null && !sabor.isEmpty()) {
                    String formattedSabor = sabor.substring(0, 1).toUpperCase() + sabor.substring(1).toLowerCase();
                    jComboBox1.addItem(formattedSabor);
                }
            }
            
            cn.close();
            
        } catch (SQLException e) {
            System.out.println("Error al cargar sabores: " + e.getMessage());
        }
        
    } 



